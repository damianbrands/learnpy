# Starting note:
# To print something in the terminal use print()

# 1-Operators
# Just like in math you can use operators in python.
# For example: 1 + 1 = 2
# It will automatically be calculated and if you print it, the result will be printed so 2.
# If you run the file you will see that the first printed thing is '2'.

print(1 + 1)

# Here is a list of the basic operators:
# '+' add two numbers so 1 + 1 = 2
# '-' subtract two numbers so 1 - 1 = 0
# '*' multiply two numbers so 2 * 4 = 8
# '/' divide two numbers so 9 / 3 = 3
# '**' exponential so 3 ** 2 = 9
# '%' modulus is the remaining amount after dividing as far as possible with whole numbers.
#       For example: 13 % 5 = 3
#       The furthest dividing by 5 goes with whole numbers is 10 this leaves us with 3 left. (The number % gives us)

print(1 - 1)
print(2 * 4)
print(9 / 3)
print(3 ** 2)
print(13 % 5)

# the order of calculations is according to PEMDAS:

print(5 * 10 % 20)

